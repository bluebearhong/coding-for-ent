public class Operators {
    public void calculate() {
        System.out.println(1 + 2);
        System.out.println(3 - 1);
        System.out.println(1 - 3);
        System.out.println(5.1 * 4);
        System.out.println(6 / 2);


        // special case
        System.out.println(5 / 2);
        System.out.println(5 % 2);
    }
}
